package com.sb;

public abstract class Exam {
	private int kor;
	private int eng;
	private int math;
	
	public int getKor() {
		return kor;
	}

	public void setKor(int kor) {
		this.kor = kor;
	}

	public int getEng() {
		return eng;
	}

	public void setEng(int eng) {
		this.eng = eng;
	}

	public int getMath() {
		return math;
	}

	public void setMath(int math) {
		this.math = math;
	}

	protected int total() {
		return kor + eng + math;
	};
}
